namespace Generator {
    /**
     * // TODO: comment Generator
     * Gets script version
     * @param fileName
     * @returns script version
     */
    export class Jump {
        public offsetY: number = 0;
        public offsetX: number = 0;

        public toString(): string {
            return "offsetX: " + this.offsetX + ", offsetY: " + this.offsetY;
        }
    }
}
